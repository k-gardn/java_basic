package variables;

public class Exam8 {
	public static void main(String[] args) {
		
		char ch = 'A';
		int num = 98;
	

		 // (자료형)변수 : cast 연산자, casting한다. // 괄호안의 자료형으로 바뀐다.
		System.out.println("ch : " + ch);
		System.out.println("(int)ch : " + (int)ch);
		
		System.out.println("num : " + num); 
		System.out.println("(char)num : " + (char)num);
	}

}
